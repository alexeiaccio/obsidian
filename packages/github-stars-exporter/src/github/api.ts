import { mkdir, readFile, stat, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import type {
	GitHubApiErrorResponse,
	GitHubStarsList,
	GitHubUser,
	StarredRepository,
} from "./types";

const GITHUB_API_BASE = "https://api.github.com";
const DEFAULT_PER_PAGE = 100;

export class GitHubApiError extends Error {
	constructor(
		message: string,
		public status?: number,
		public response?: GitHubApiErrorResponse,
	) {
		super(message);
		this.name = "GitHubApiError";
	}
}

export class GitHubClient {
	private token: string;

	constructor(token: string) {
		this.token = token;
	}

	private async makeRequest<T>(
		url: string,
		options: RequestInit = {},
	): Promise<{ data: T; headers: [string, string][] }> {
		const headers: HeadersInit = {
			Accept: "application/vnd.github.v3.star+json",
			"X-GitHub-Api-Version": "2022-11-28",
			"User-Agent": "Obsidian-GitHub-Stars-Exporter",
		};

		if (this.token) {
			headers.Authorization = `Bearer ${this.token}`;
		}

		const response = await fetch(url, {
			...options,
			headers,
		});

		if (!response.ok) {
			let errorData: GitHubApiErrorResponse | undefined;
			try {
				errorData = await response.json();
			} catch {
				// Ignore JSON parse errors
			}

			throw new GitHubApiError(
				errorData?.message || `HTTP ${response.status}: ${response.statusText}`,
				response.status,
				errorData,
			);
		}

		return {
			headers: [...response.headers.entries()],
			data: await response.json(),
		};
	}

	async me(): Promise<GitHubUser> {
		try {
			const user = await this.makeRequest<GitHubUser>(
				`${GITHUB_API_BASE}/user`,
			);
			return user.data;
		} catch (error) {
			console.error("Failed to fetch user:", error);
			throw error;
		}
	}

	async validateToken(): Promise<boolean> {
		try {
			await this.me();
			return true;
		} catch (error) {
			console.error("Token validation failed:", error);
			return false;
		}
	}

	async getStarredRepositories(
		maxStars: number = 1000,
		onProgress?: (fetched: number, total: number) => void,
	): Promise<{ repositories: StarredRepository[]; totalCount: number }> {
		const repositories: StarredRepository[] = [];
		let page = 1;
		let count = 0;
		let totalCount = 0;

		try {
			// First, get the total count
			const firstResponse = await this.localCache(
				`${GITHUB_API_BASE}/user/starred?per_page=1&page=1`,
				() =>
					this.makeRequest<StarredRepository[]>(
						`${GITHUB_API_BASE}/user/starred?per_page=1&page=1`,
					),
				JSON.parse,
				JSON.stringify,
			);

			// Extract total count from Link header
			const linkHeader = firstResponse.headers.find(
				([key]) => key === "link",
			)?.[1];
			const match = linkHeader?.match(/page=(\d+)>; rel="last"/);
			if (match) {
				totalCount = Number(match[1]);
				count = Math.min(totalCount, maxStars);
			}

			do {
				const url = `${GITHUB_API_BASE}/user/starred?per_page=${Math.min(DEFAULT_PER_PAGE, maxStars)}&page=${page}`;
				const pageData = await this.localCache(
					url,
					() => this.makeRequest<StarredRepository[]>(url),
					JSON.parse,
					JSON.stringify,
				);

				repositories.push(...pageData.data);

				if (onProgress) {
					onProgress(repositories.length, count || repositories.length);
				}

				// Stop if we've reached maxStars or there are no more results
				if (
					repositories.length >= maxStars ||
					pageData.data.length < DEFAULT_PER_PAGE
				) {
					break;
				}

				page++;
			} while (repositories.length < maxStars);

			return { repositories, totalCount };
		} catch (error) {
			console.error("Failed to fetch starred repositories:", error);
			throw error;
		}
	}

	async getReadmeContent(owner: string, repo: string): Promise<string | null> {
		try {
			const readmeData = await this.makeRequest(
				`${GITHUB_API_BASE}/repos/${owner}/${repo}/readme`,
			);

			if (
				readmeData &&
				typeof readmeData === "object" &&
				"content" in readmeData
			) {
				// GitHub API returns base64 encoded content
				const content = (readmeData as any).content;
				return atob(content);
			}

			return null;
		} catch (error) {
			console.warn(`Failed to fetch README for ${owner}/${repo}:`, error);
			return null;
		}
	}

	getRateLimitInfo(): Promise<any> {
		return this.makeRequest(`${GITHUB_API_BASE}/rate_limit`);
	}

	async localCache<T>(
		key: string,
		fn: () => Promise<T>,
		parse: (r: string) => NoInfer<T> = (r) => r as T,
		stringify: (r: unknown) => string = (r) => String(r),
	): Promise<T> {
		const folder = resolve(process.cwd(), "node_modules/.cache");
		const path = resolve(folder, encodeURIComponent(key));
		try {
			await stat(path);
		} catch {
			await mkdir(folder, { recursive: true });
		}
		try {
			return parse(await readFile(path, { encoding: "utf-8" }));
		} catch {
			const result = await fn();
			await writeFile(path, stringify(result), { encoding: "utf-8" });
			return result;
		}
	}

	async getStarredLists(): Promise<GitHubStarsList[]> {
		try {
			const lists: GitHubStarsList[] = [];
			const me = await this.me();

			const html = await this.localCache(
				`https://github.com/${me.login}?tab=stars`,
				() =>
					fetch(`https://github.com/${me.login}?tab=stars`).then((r) =>
						r.text(),
					),
			);
			const matches = html.matchAll(/\/lists\/(.+)"/g);

			for (const match of matches) {
				if (!match?.[1]) continue;
				const repos: string[] = [];
				let page = 1;
				let totalCount = 0;

				do {
					const listHtml = await this.localCache(
						`https://github.com/stars/${me.login}/lists/${match[1]}?page=${page}`,
						() =>
							fetch(
								`https://github.com/stars/${me.login}/lists/${match[1]}?page=${page}`,
							).then((r) => r.text()),
					);
					const repositories = Number(
						listHtml.match(/(\d+)\srepositories/)?.[1],
					);

					if (listHtml === "Not Found" || Number.isNaN(repositories)) break;

					totalCount = repositories;
					const listMatches = listHtml.matchAll(
						/href="(.+)">\n.+?<span class="text-normal"/gm,
					);

					for (const listMatch of listMatches) {
						repos.push(listMatch[1]);
					}

					page++;
				} while (repos.length < totalCount);

				lists.push({ name: match[1], repos });
			}

			return lists;
		} catch (error) {
			console.error("Failed to fetch starred lists:", error);
			throw error;
		}
	}
}

